<Section title="Chapter 5 Reading Response">
    <p>
        I wanted to respond to the piece <i>On the Floor</i>, introduced in <ArtDes />, chapter 5,
        page 265. Reading the examples of all the cool and wacky instruments in the pages beforehand
        were interesting for sure, but <i>On the Floor</i> is the one that really blew my mind.
    </p>
    <p>
        Let me explain why. I've always been used to traditional orchestras, consisting of many
        performers playing a variety of instruments. One of the coolest aspects of these orchestras
        is the sheer amount of skill and proficiency each musician has over their own instruments.
        They've spent years practicing and performing. They know the ins and outs of how to produce
        exactly the sound they want. They are in control.
    </p>
    <p>
        And then, in <i>On the Floor</i>, people are playing slot machines. Gambling. One of the
        most out of control activities you can do! All you need to do to play is push a few buttons.
        You could probably grab someone out of the audience, teach them, and have them performing in
        a few minutes.
    </p>
    <p>
        But all the same, these slot machines are inarguably the instruments within this orchestra.
        There is still absolutely a sense of coherence, a sense of musicality, produced by this
        piece. And I think this has caused me to kind of broaden the idea of musical performance
        within my mind. I can begin to look past the technical aspects of the performance, in order
        to appreciate the concepts and the messages being conveyed in the music. I won't always
        judge the quality of a performance solely by how skilled the musicians are. Of course, this
        doesn't mean that I won't still be impressed by a performer showing off their technical
        ability. But I can appreciate that, even if people are just pulling the simulated arm of a
        slot machine, they can still be musicians, creating a piece of music just as meaningful as
        any other.
    </p>
    <p></p>
    <p>
        I originally wanted to end my response there, but I also want to comment on the creativity
        of this piece. Having people essentially play a game and having that produce the music is
        such a cool idea, and quite a novel one too. It really harnesses the randomness of computers
        and makes full use of the idea of customizing the mapping from input to sound. The concept
        is really thought-provoking and opens my mind to lots more possibilities of computer music.
    </p>
    <p>
